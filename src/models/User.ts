import { Schema, model } from "mongoose";
import { User } from "../types";

const User = new Schema<User>({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
    select: false,
  },
});

export default model<User>("User", User);
